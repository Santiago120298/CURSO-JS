//operadores relacionales
let a = 10
console.log(a > 5);// boolean
console.log(a >= 5);// boolean
console.log(a < 5);// boolean
console.log(a <= 5);// boolean


//operadores de igualdad
console.log(a ==10); // evaluando si la valor es exactamente a 10
console.log(a !=10); // evaluando si el valor NO es exactamente a 10
// Mas usados y mejores
console.log(a ==='10'); //=== compara si es exactamente el mimso tipo de dato y valor
